package class19;

public class FindMin {

    public int minNum(int a, int b) {
        if (a < b) {
            return a;
        }
        else {
            return b;
        }
    }

    public double minNum (double a, double b) {

        if (a < b) {
            return a;
        }
        else {
            return b;
        }

    }

}
