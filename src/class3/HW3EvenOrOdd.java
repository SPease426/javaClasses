package class3;

public class HW3EvenOrOdd {
    public static void main(String[] args) {

        int numVal = 2;

        if (numVal % 2 == 0) {
            System.out.println(numVal + " is an even number");
        }
        else if (numVal % 2 == 1) {
            System.out.println(numVal + " is an odd number");
        }
    }
}
